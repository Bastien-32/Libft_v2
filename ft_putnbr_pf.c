#include "ft_printf.h"

int	ft_putnbr_pf(int n)
{
	int		i;
	char	c;

	i = 0;
	if (n == -2147483648)
	{
		i += write (1, "-2147483648", 11);
		return (i);
	}
	if (n < 0)
	{
		n = -n;
		i += write (1, "-", 1);
	}
	if (n >= 10)
		i += ft_putnbr_pf(n / 10);
	c = (n % 10) + '0';
	i += write(1, &c, 1);
	return (i);
}
